/**
 * Filters Indonesian cities based on input text
 * @param text Search text
 * @param limit Maximum number of results to return
 * @returns Array of matching city names
 */
export function filterCities(text: string, limit: number = 5): string[] {
  if (!text) return [];
  
  const normalizedText = text.toLowerCase().trim();
  
  return indonesianCities
    .filter(city => city.toLowerCase().includes(normalizedText))
    .slice(0, limit);
}
